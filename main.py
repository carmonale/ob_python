import operaciones

def main():
    ressuma = operaciones.suma(2,2)
    print("Resultado de la suma:",ressuma)

    resresta = operaciones.resta(2,2)
    print("Resultado de la resta:",resresta)

    resmulti = operaciones.multi(2,2)
    print("Resultado de la multi:",resmulti)

    resdivi = operaciones.divi(2,2)
    print("Resultado de la division:",resdivi)

if __name__ == "__main__":
    main()
